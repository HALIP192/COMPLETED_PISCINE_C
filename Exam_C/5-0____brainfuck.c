#include <unistd.h>
#include <stdlib.h>

#define BUFF_SIZE 2048

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int		find_next(char *str)
{
	int		count;
	int		i;

	i = 1;
	count = 0;
	while (str[i])
	{
		if (str[i] == '[')
			count++;
		else if (str[i] == ']')
		{
			if (count == 0)
				return (i);
			else
				count--;
		}
		i++;
	}
	return (0);
}

int		find_previous(char *str)
{
	int		count;
	int		i;

	i = -1;
	count = 0;
	while (str[i])
	{
		if (str[i] == ']')
			count++;
		else if (str[i] == '[')
		{
			if (count == 0)
				return (i);
			else
				count--;
		}
		i--;
	}
	return (0);
}

int		brainfuck(char *str)
{
	char	*ptr;
	int		i;
	int		j;

	if (!(ptr = (char *)malloc(sizeof(char) * (BUFF_SIZE + 1))))
		return (-1);
	j = 0;
	while (j <= BUFF_SIZE)
		ptr[j++] = '\0';
	j = 0;
	i = 0;
	while (str[i])
	{
		if (str[i] == '>')
			j++;
		else if (str[i] == '<')
			j--;
		else if (str[i] == '+')
			ptr[j]++;
		else if (str[i] == '-')
			ptr[j]--;
		else if (str[i] == '.')
			ft_putchar(ptr[j]);
		if ((str[i] == '[') && !ptr[j])
			i += find_next(str + i);
		else if ((str[i] == ']') && ptr[j])
			i += find_previous(str + i);
		else
			i++;
	}
	free(ptr);
	return (0);
}

int		main(int ac, char **av)
{
	if (ac == 2)
		brainfuck(av[1]);
	else
		ft_putchar('\n');
	return (0);
}



