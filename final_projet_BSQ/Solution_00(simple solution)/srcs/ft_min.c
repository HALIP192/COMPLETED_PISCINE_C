#include "../header/bsq.h"

t_uns		ft_min(t_uns a, t_uns b, t_uns c)
{
	if ((a <= b) && (a <= c))
		return (a);
	else if ((b <= a) && (b <= c))
		return (b);
	else
		return (c);
}
